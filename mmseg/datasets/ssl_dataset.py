# ---------------------------------------------------------------
# SSLDataset: Semi-Supervised Learning Dataset
#
# Pairs labeled and unlabeled samples from the SAME domain.
# The labeled subset is specified via a split file listing image
# basenames (without suffix); all remaining images are unlabeled.
#
# Supports Rare Class Sampling (RCS) on the labeled subset to
# mitigate class imbalance — critical for satellite land-cover
# where some categories have very few labeled pixels.
# ---------------------------------------------------------------

import json
import os.path as osp

import mmcv
import numpy as np
import torch

from .builder import DATASETS


def get_rcs_class_probs(data_root, temperature):
    """Compute class sampling probabilities for Rare Class Sampling."""
    with open(osp.join(data_root, 'sample_class_stats.json'), 'r') as of:
        sample_class_stats = json.load(of)
    overall_class_stats = {}
    for s in sample_class_stats:
        s.pop('file')
        for c, n in s.items():
            c = int(c)
            if c not in overall_class_stats:
                overall_class_stats[c] = n
            else:
                overall_class_stats[c] += n
    overall_class_stats = {
        k: v
        for k, v in sorted(
            overall_class_stats.items(), key=lambda item: item[1])
    }
    freq = torch.tensor(list(overall_class_stats.values()))
    freq = freq / torch.sum(freq)
    freq = 1 - freq
    freq = torch.softmax(freq / temperature, dim=-1)
    return list(overall_class_stats.keys()), freq.numpy()


@DATASETS.register_module()
class SSLDataset(object):
    """Semi-supervised learning dataset.

    Pairs a labeled subset with an unlabeled subset from the same
    domain. The labeled subset is defined by a split file; the
    unlabeled subset contains all images (labels are NOT used during
    training — pseudo-labels are generated by the EMA teacher).

    The data loading interface mirrors UDADataset: each __getitem__
    returns a dict with both labeled sample keys (img, img_metas,
    gt_semantic_seg) and unlabeled sample keys (target_img,
    target_img_metas), enabling reuse of the DAPCN training loop.

    Args:
        labeled (dict): Config for the labeled subset dataset.
            Must include a ``split`` field pointing to a text file
            listing labeled image basenames.
        unlabeled (dict): Config for the unlabeled subset dataset.
            Typically the full training set (labels exist on disk
            but are only used for evaluation, not training).
        cfg (dict): Additional config with optional
            ``rare_class_sampling`` settings.

    Data directory structure::

        data/satellite/
        ├── images/
        │   ├── img_001.png
        │   ├── img_002.png
        │   └── ...
        ├── labels/
        │   ├── img_001.png
        │   ├── img_002.png
        │   └── ...
        └── splits/
            └── labeled.txt    # one basename per line: img_001
    """

    def __init__(self, labeled, unlabeled, cfg):
        self.labeled = labeled
        self.unlabeled = unlabeled
        self.ignore_index = labeled.ignore_index
        self.CLASSES = labeled.CLASSES
        self.PALETTE = labeled.PALETTE
        assert unlabeled.CLASSES == labeled.CLASSES
        assert unlabeled.PALETTE == labeled.PALETTE

        mmcv.print_log(
            f'SSLDataset: {len(self.labeled)} labeled, '
            f'{len(self.unlabeled)} unlabeled samples', 'mmseg')

        # Rare Class Sampling on labeled subset
        rcs_cfg = cfg.get('rare_class_sampling')
        self.rcs_enabled = rcs_cfg is not None
        if self.rcs_enabled:
            self.rcs_class_temp = rcs_cfg['class_temp']
            self.rcs_min_crop_ratio = rcs_cfg['min_crop_ratio']
            self.rcs_min_pixels = rcs_cfg['min_pixels']

            self.rcs_classes, self.rcs_classprob = get_rcs_class_probs(
                cfg['labeled']['data_root'], self.rcs_class_temp)
            mmcv.print_log(f'RCS Classes: {self.rcs_classes}', 'mmseg')
            mmcv.print_log(f'RCS ClassProb: {self.rcs_classprob}', 'mmseg')

            with open(
                    osp.join(cfg['labeled']['data_root'],
                             'samples_with_class.json'), 'r') as of:
                samples_with_class_and_n = json.load(of)
            samples_with_class_and_n = {
                int(k): v
                for k, v in samples_with_class_and_n.items()
                if int(k) in self.rcs_classes
            }
            self.samples_with_class = {}
            for c in self.rcs_classes:
                self.samples_with_class[c] = []
                for file, pixels in samples_with_class_and_n[c]:
                    if pixels > self.rcs_min_pixels:
                        self.samples_with_class[c].append(
                            file.split('/')[-1])
                assert len(self.samples_with_class[c]) > 0
            self.file_to_idx = {}
            for i, dic in enumerate(self.labeled.img_infos):
                file = dic['ann']['seg_map']
                self.file_to_idx[file] = i

    def get_rare_class_sample(self):
        """Sample a labeled image biased toward rare classes."""
        c = np.random.choice(self.rcs_classes, p=self.rcs_classprob)
        f1 = np.random.choice(self.samples_with_class[c])
        i1 = self.file_to_idx[f1]
        s1 = self.labeled[i1]
        if self.rcs_min_crop_ratio > 0:
            for j in range(10):
                n_class = torch.sum(s1['gt_semantic_seg'].data == c)
                if n_class > self.rcs_min_pixels * self.rcs_min_crop_ratio:
                    break
                s1 = self.labeled[i1]

        # Sample an unlabeled image
        i2 = np.random.choice(range(len(self.unlabeled)))
        s2 = self.unlabeled[i2]

        return {
            **s1,
            'target_img_metas': s2['img_metas'],
            'target_img': s2['img']
        }

    def __getitem__(self, idx):
        if self.rcs_enabled:
            return self.get_rare_class_sample()
        else:
            s1 = self.labeled[idx % len(self.labeled)]
            s2 = self.unlabeled[idx % len(self.unlabeled)]
            return {
                **s1,
                'target_img_metas': s2['img_metas'],
                'target_img': s2['img']
            }

    def __len__(self):
        return max(len(self.labeled), len(self.unlabeled))
